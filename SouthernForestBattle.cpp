void SouthernForestBattle::loadLayers(){

    Layer *backgroundLayer = new Layer(imageStore->getBitMap("desertRegion") ,
        15 , 20 , this->backgroundLayerLayout , 300);

    this->loadLayer(backgroundLayer);
}

